      'BackgroundColor',figure_color, ...
      'Position',[17 117 60 20], ...
      'CallBack','figure(findobj(''tag'',''results_figure''));zoom on;set(findobj(''tag'',''zoom off''),''value'',0)',...
      'String','zoom on', ...
      'Style','radiobutton', ...
