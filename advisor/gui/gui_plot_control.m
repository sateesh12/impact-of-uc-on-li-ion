      'BackgroundColor',figure_color, ...
      'Position',[17 174 60 19], ...
      'String','hold on', ...
      'Style','radiobutton', ...
      'Tag','hold on');
   h1 = uicontrol('Parent',h0, ...
      'BackgroundColor',figure_color, ...
      'CallBack','figure(findobj(''tag'',''results_figure''));hold off;set(findobj(''tag'',''hold on''),''value'',0)',...
      'Position',[17 152 60 20], ...
      'String','hold off', ...
      'Style','radiobutton', ...
      'Tag','hold off');
